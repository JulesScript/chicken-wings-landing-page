   // Function to simulate the typewriter effect
   function typeWriter(textElement, delay) {
    const text = textElement.dataset.words.split(':');
    const colors = textElement.dataset.colors.split(':');
    let wordIndex = 0;
    let charIndex = 0;
    let isDeleting = false;
  
    function type() {
      const currentColor = colors[wordIndex % colors.length];
      textElement.style.color = currentColor;
  
      const currentWord = text[wordIndex];
      const typeDelay = isDeleting ? delay / 2 : delay;
  
      if (charIndex < currentWord.length && !isDeleting) {
        textElement.textContent += currentWord[charIndex];
        charIndex++;
      } else if (isDeleting && charIndex > 0) {
        textElement.textContent = currentWord.substring(0, charIndex - 1);
        charIndex--;
      } else {
        isDeleting = !isDeleting;
        if (!isDeleting) {
          wordIndex++;
          // If all words have been displayed, reset to the first word
          if (wordIndex >= text.length) {
            wordIndex = 0;
          }
        }
      }
  
      setTimeout(type, typeDelay);
    }
  
    // Start the typing process on page load
    document.addEventListener('DOMContentLoaded', () => {
      type();
    });
  }
  
  const typerElement = document.getElementById('some-id');
  const delay = parseInt(typerElement.dataset.delay, 10) || 200;
  typeWriter(typerElement, delay);